# # relational operators
# **   (a^b)
# == equality check
# != not equal to
# >= greater than <=less than
a=50
b=20
print(a**b)        #a^b
print(a == b)      #false
print(a != b)      #true
print(a>=b)        #true
print(a<=b)        #false

