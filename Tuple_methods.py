# tuple methods
tup=(1,2,3,5,3,2,2)

# to get index of first occurence of any element
print(tup.index(3))
# to get elemnt of specific index
print(tup[2])
# to get last value of index
print(tup[-1])
# count total occurences of a element
print(tup.count(2))
# to get nested tuple
tuple=("abc",(1,3,5))
print(tuple)
print(tuple[1][1])

# to concatenate tuples
t=(1,2,3)+("akuu",3,6,7,8)
print(t)

# to get repeated elements
t1=((2,)*3,4,5,67,4)
print(t1)
# to find min
print(min(tup))
# to find max
print(max(tup))
# to find sum
print(sum(tup))
